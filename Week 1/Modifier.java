//Use of different access modifiers in java
public class Modifier
{
    String name = "Rohan";
    public int age = 22;
    protected int salary = 12300;
    void method1()
    {
        System.out.println("Name: " +name);
    }
    public void method2()
    {
        System.out.println("Age: " +age);
    }
    public void method3(){
        System.out.println("Salary: " +salary);
    }
}


